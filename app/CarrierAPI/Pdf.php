        $shipReason = $shipment->ship_reason;
        if (isset($shipment->ship_reason)) {
            $reason = \App\Models\ShipReason::where('code', $shipment->ship_reason)->first();
            if ($reason) {
                $shipReason = $reason->description;
            }
