    /**
     * @param  type  $filename
     * @param  type  $encoded
     * @param  type  $output
     *
     * @return type
     */
    public function displayPdf($filename, $encoded)
    {
        if ($encoded) {
            return base64_encode($this->pdf->Output($filename, $this->output));
        } else {
            return $this->pdf->Output($filename, $this->output);
        }
    }

